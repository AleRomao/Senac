    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
       String tipoPC = "";
        if(rboPC.isSelected()){
            tipoPC = rboPC.getText();
        }else if (rboNotebook.isSelected()){
            tipoPC = rboNotebook.getText();
        }else if ( rboServidor.isSelected()){
            tipoPC = rboServidor.getText();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um tipo de computador");
        }
        
            //Sisyema Operacional
            String tipoSO = "";
        if (tgWindows.isSelected()){
            tipoSO = tgWindows.getText();
        }else if (tgLinux.isSelected()){
            tipoSO = tgWindows.getText();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Selecionar um sistema operacional");
        }
        //HD
        String hd = cboHD.getSelectedItem().toString();
        
        
        //Acessorios
        String acessorios = "";
        if (chkMochila.isSelected()){
            acessorios += chkMochila.getText() + " ,";
        }
        
        if (chkMouse.isSelected()){
            acessorios += chkMouse.getText() + " ,";
        }
        
        if (chkTeclado.isSelected()){
            acessorios += chkTeclado.getText() + " ,";
        }
        //garantias
        List<String> garantiasSelecionadas = lstGarantia.getSelectedValuesList();
        
        String garantiasConcatenadas = "";
        
        for (String garantia : garantiasSelecionadas) {
            garantiasConcatenadas += garantia + " ,";
        }
        
        
        //Adicionar à tabela
        DefaultTableModel modelo = (DefaultTableModel) tblComputadores.getModel();
        
        //adicionar uma linha a tabela
        modelo.addRow(new String[]{
            tipoPC,
            tipoSO,
            hd,
            acessorios,
            garantiasConcatenadas
        });
        
    }//GEN-LAST:event_btnAdicionarActionPerformed
    
    
    
    
