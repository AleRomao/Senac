    private void btnDetalhesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetalhesActionPerformed

        int linhaSelecionada  = tblComputadores.getSelectedRow();

        if(linhaSelecionada >=0){
            //Exibos os detalhes da linha

            JOptionPane.showMessageDialog(rootPane,"Dados do Item: \n" +
                    tblComputadores.getValueAt(linhaSelecionada, 0) + "\n" +
                    tblComputadores.getValueAt(linhaSelecionada, 1) + "\n" +
                    tblComputadores.getValueAt(linhaSelecionada, 2) + "\n" +
                    tblComputadores.getValueAt(linhaSelecionada, 3) + "\n" +
                    tblComputadores.getValueAt(linhaSelecionada, 4) + "\n"
                    );
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
        
    }//GEN-LAST:event_btnDetalhesActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         int linhaSelecionada  = tblComputadores.getSelectedRow();
         
         DefaultTableModel model = (DefaultTableModel)tblComputadores.getModel();
         
         if(linhaSelecionada>=0)
             model.removeRow(linhaSelecionada);
         else
                JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
    }//GEN-LAST:event_btnExcluirActionPerformed

